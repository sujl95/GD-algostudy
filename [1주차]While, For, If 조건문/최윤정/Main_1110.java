package study1;

/*
 * 첫째 줄에 N이 주어진다. 
 * N은 0보다 크거나 같고, 99보다 작거나 같은 정수이다.
 * 첫째 줄에 N의 사이클 길이를 출력한다.
 * 먼저 주어진 수가 10보다 작다면 
 * 		앞에 0을 붙여 두 자리 수로 만들고, 각 자리의 숫자를 더한다
 * 주어진 수의 가장 오른쪽 자리 수와
 *  	앞에서 구한 합의 가장 오른쪽 자리 수를 이어 붙이면 새로운 수를 만들 수 있다
 *  26부터 시작한다. 
 *  2+6 = 8이다. 
 *  새로운 수는 68이다. 
 *  6+8 = 14이다. 
 *  새로운 수는 84이다. 
 *  8+4 = 12이다. 
 *  새로운 수는 42이다. 
 *  4+2 = 6이다. 
 *  새로운 수는 26이다.
 */
import java.util.*;

public class Main_1110 {
	public static void main(String[] args) throws Exception {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int number = n;
		int cycle=0;
		do{
			number = number%10*10+(number/10+number%10)%10;
			cycle++;
		}while(n!=number);
		System.out.println(cycle);
	}
}
